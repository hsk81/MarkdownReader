import UUID from '../util/uuid';

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

export interface IBaseStorage {
    [arg:string]:any;
}

export interface IBaseStorage {
    getProperty:<T>(path:string, options?:{
        fallback:T
    }) => T;
    setProperty:<T>(path:string, value:T, options?:{
        file:boolean, timeout:number
    }) => void;
    deleteProperty:(path:string) => void;
}

export interface IBaseStorage {
    subscribeToProperty:(path:string, callback:Function) => UUID;
    unsubscribeProperty:(uuid:UUID) => void;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

export default IBaseStorage;

